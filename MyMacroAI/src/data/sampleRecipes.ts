/**
 * Get recipes by category
 */
export function getSampleRecipesByCategory(category: string): PublicRecipe[] {
    if (IS_PRODUCTION) return [];
    return SAMPLE_RECIPES.filter(recipe =>
        recipe.categories.includes(category)
    );
}

/**
 * Get random sample recipes for "Lucky Picks" feature
 */
export function getRandomSampleRecipes(count: number = 3): PublicRecipe[] {
    if (IS_PRODUCTION) return [];
    const shuffled = [...SAMPLE_RECIPES].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count);
}

/**
 * Get a sample recipe by ID
 */
export function getSampleRecipeById(id: string): PublicRecipe | undefined {
    if (IS_PRODUCTION) return undefined;
    return SAMPLE_RECIPES.find(recipe => recipe.id === id);
}

